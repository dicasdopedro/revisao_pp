# Criando uma lista (vetor)
meu_vetor = [10, 20, 30, 40, 50]

# Acessando o primeiro elemento (índice 0)
primeiro_elemento = meu_vetor[0]
print(primeiro_elemento)  # Saída: 10

# Acessando o terceiro elemento (índice 2)
terceiro_elemento = meu_vetor[2]
print(terceiro_elemento)  # Saída: 30

# Acessando o último elemento (índice -1)
ultimo_elemento = meu_vetor[-1]
print(ultimo_elemento)  # Saída: 50

# Acessando o penúltimo elemento (índice -2)
penultimo_elemento = meu_vetor[-2]
print(penultimo_elemento)  # Saída: 40

# Acessando do segundo ao quarto elemento (índices 1 a 3)
sublista = meu_vetor[1:4]
print(sublista)  # Saída: [20, 30, 40]

